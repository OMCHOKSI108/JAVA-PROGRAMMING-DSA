//Relatinal Operator '=','>','<','=>','=<','=='
class relational {
    public static void main(String[] args) {
           int A = 10;
           int B = 30;
           System.out.println((A==B));    //Flase
           System.out.println((A>B));     //False
           System.out.println((A<B));     //True  
           System.out.println((A>=B));    //False
           System.out.println((A<=B));    //True  
             
    }    
}
